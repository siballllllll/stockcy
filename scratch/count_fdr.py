raw = copy.deepcopy(US_SECTOR_MAP)
existing = {s['ticker'] for subs in raw.values() for stks in subs.values() for s in stks}
print(f'큐레이션: {len(existing)}개')

frames = []
for market, exch in [('NASDAQ','NASDAQ'),('NYSE','NYSE'),('AMEX','AMEX')]:
    try:
        df = fdr.StockListing(market).copy()
        df['_exchange'] = exch
        frames.append(df)
        print(f'{market}: {len(df)}개')
    except Exception as e:
        print(f'{market} 실패: {e}')

all_df = pd.concat(frames, ignore_index=True)
all_df.columns = [str(c).strip() for c in all_df.columns]
cols = {c.lower(): c for c in all_df.columns}
sym_col  = cols.get('symbol', cols.get('code', cols.get('ticker')))
ind_col  = cols.get('industry', cols.get('industrycode'))
print(f'FDR 총 행: {len(all_df)}개, ind_col={ind_col}')

# 실제 Industry 값 샘플 출력
if ind_col:
    sample_inds = all_df[ind_col].dropna().unique()[:20]
    print(f'\nIndustry 샘플 (처음 20개):')
    for v in sample_inds:
        print(f'  "{v}"')

FDR_SUB_CAP = 20
fdr_sub_cnt: dict = {}
added = 0
sec_counts = {}
for _, row in all_df.iterrows():
    sym = str(row.get(sym_col,'')).strip().upper()
    if not sym or not (1<=len(sym)<=5) or not sym.isalpha(): continue
    if sym in existing: continue
    ind = str(row.get(ind_col,'') if ind_col else '').strip()
    mapping = FDR_IND_MAP.get(ind)
    if not mapping: continue
    kr_sec, kr_sub = mapping
    if kr_sec not in raw: continue
    sub_key = (kr_sec, kr_sub)
    if fdr_sub_cnt.get(sub_key, 0) >= FDR_SUB_CAP: continue
    existing.add(sym)
    fdr_sub_cnt[sub_key] = fdr_sub_cnt.get(sub_key, 0) + 1
    added += 1
    sec_counts[kr_sec] = sec_counts.get(kr_sec, 0) + 1

print(f'\nFDR 추가: {added}개')
